package basic;

public class DoWhileEx1 {

	public static void main(String[] args) {
		
		for(int i = 10; i < 10; i ++) {
			System.out.println("for : "+i);
		}
		System.out.println();
		
		int i = 10;
		
		while(i < 10) {
			System.out.println("while : "+i);
		}
		System.out.println();
		
		do {														//do while : 조건이 맞지 않아도 한번은 돈다
			System.out.println("do ~ while : "+i);
		}while(i < 10);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
