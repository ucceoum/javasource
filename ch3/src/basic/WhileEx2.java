package basic;

public class WhileEx2 {

	public static void main(String[] args) {
		// 1~10까지 합계 구하기
		
		
		int sum = 0;
		
		for(int i = 1; i <= 10; i++) {
			sum += i;
		}
		System.out.println("1~10 합계 : "+sum);
		
		
		int j = 1;
		int hap = 0;
				
				
		while(j <= 10) {
			hap += j;
			j++;
		}
		
		System.out.println("1~10 합계 : "+hap);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
