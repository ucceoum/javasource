package basic;

public class ForEx3 {

	public static void main(String[] args) {
		// 1~10까지의 숫자 중에서 짝수만 출력하기
		// 2,4,6,8,10
		for(int i = 2; i < 11; i+=2) {
			System.out.println(i);
		}
		
		//1~100까지의 숫자 중에서 3의 배수만 출력하기
		
		for(int i = 3; i<101; i+=3) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
