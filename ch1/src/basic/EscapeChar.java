package basic;

public class EscapeChar {

	public static void main(String[] args) {
		//   escape : \(역슬래쉬)가 붙은 문자 리터럴
		System.out.println("탭 : \t 만큼 이동");
		System.out.println("줄바꿈 : \n");
		System.out.println("리턴 : \r");
		System.out.println("큰 따옴표 : \"아름다운우리나라\"");
		System.out.println("작은 따옴표 : \'아름다운우리나라\'");
		System.out.println("역슬래쉬 : \\");
	}

}
