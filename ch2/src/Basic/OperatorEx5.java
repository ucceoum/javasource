package Basic;

public class OperatorEx5 {

	public static void main(String[] args) {
		int i1=10;
		int i2=20;
		
		System.out.println("i1 + i2 = "+(i1+i2));
		System.out.println("i1 - i2 = "+(i1-i2));
		System.out.println("i1 * i2 = "+(i1*i2));
		System.out.println("i1 / i2 = "+(i2/i1));	//몫
		System.out.println("i1 % i2 = "+(i2%i1)); 	//나머지
		System.out.println("5/3 = "+(5/3));
		System.out.println("5%3 = "+(5%3));
		
	}

}
