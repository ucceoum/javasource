package Basic;

public class OperatorEx8 {

	public static void main(String[] args) {
			float f1=1.0f;
			double d1=1.0d;
			System.out.println(f1 == d1);
			System.out.println(f1 != d1);
			
			
			float f2=0.1f;
			double d2=0.1d;
			System.out.println("f2 = "+f2);
			System.out.println("d2 = "+d2);
			
			System.out.println(f2 == d2);
			System.out.println(f2 != d2);
		
		
		
		
	}

}
