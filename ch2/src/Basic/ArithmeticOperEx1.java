package Basic;

public class ArithmeticOperEx1 {

	public static void main(String[] args) {
		//대입연산자 : =
		//복합대입연산자 : +=, *=, -=, /=, %= ....
		int v1 = 5;
		int v2 = 2;
		int result = v1 + v2;
		System.out.println("result = "+result);
		result = v1-v2;
		System.out.println("result = "+result);
		result = v1*v2;
		System.out.println("result = "+result);
		result = v1/v2;
		System.out.println("result = "+result);
		result = v1%v2;
		System.out.println("result = "+result);
		
		
		
		
		
	}

}
