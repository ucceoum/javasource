package Basic;

public class OperatorEx7 {
public static void main(String[] args) {
		// 비교 연산자 : ==, !=, <, >, <=, >=, instanceof
		// 결과값 : boolean 타입으로 나옴
		int num1 = 10;
		int num2 = 10;
		System.out.println(num1==num2); // == : 같음
		System.out.println(num1!=num2); // != : 같지않음
		System.out.println(num1<num2);
		System.out.println(num1<=num2);
	
		char ch1='A';
		char ch2='B';
		System.out.println(ch1<ch2); 	// 65 < 66  (아스키코드값)
										// A : 65, a = 97
			
			
		
}
}
