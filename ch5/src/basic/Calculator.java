package basic;

public class Calculator {
	//사칙연산 메소드
	
	
	
	//덧셈 = 두 개의 숫자를 받아 더하기 한 후 결과 리턴
	int add(int a, int b) {
		return a+b;
	}
	
	//뺄셈
	int subtract(int a, int b) {
		return a-b;
	}
	
	
	//나눗셈
	double divide(double a, double b) {
		return a/b;
	}
	
	//곱셈
	int multiply(int a, int b) {
		return a*b;
	}
	
	
	
	
	
	
	
	
}
